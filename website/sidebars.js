// sidebars.js
const fs = require('fs');
const path = require('path');

// simple beautify: replace underscores with spaces
function beautify(label) {
  return label.replace(/_/g, ' ');
}

function generateSidebar(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  return entries
    .filter(e => e.isDirectory())
    .map(e => ({
      type: 'category',
      label: beautify(e.name),
      collapsed: true,
      items: [{ type: 'autogenerated', dirName: path.relative('docs', path.join(dir, e.name)) }],
    }));
}

module.exports = {
  tutorialSidebar: [
    {
      type: 'category',
      label: 'M.Sc. Mathematics',
      collapsed: false,
      items: generateSidebar('docs'),
    },
  ],
};
